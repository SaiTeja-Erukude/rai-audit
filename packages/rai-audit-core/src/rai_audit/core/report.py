# ── Markdown ──────────────────────────────────────────────────────────────────

def render_markdown(report: AuditReport) -> str:
    lines: list[str] = []

    lines.append(f"# Audit Report: {report.project_name}")
    lines.append(f"\n**Audit type:** {report.audit_type}")
    lines.append(f"**Overall risk:** {report.overall_risk_level.value.upper()}")
    if report.overall_score is not None:
        lines.append(f"**Score (heuristic):** {report.overall_score:.1f}/100")

    lines.append("\n## Risk Summary\n")
    lines.append("| Category | Risk Level | Findings | Passed |")
    lines.append("|----------|-----------|----------|--------|")
    for cat in report.risk_matrix:
        lines.append(
            f"| {cat.category} | {cat.risk_level.value.upper()} "
            f"| {cat.finding_count} | {cat.passed_count} |"
        )

    active = [f for f in report.findings if f.severity != Severity.PASSED]
    passed = [f for f in report.findings if f.severity == Severity.PASSED]

    if active:
        lines.append("\n## Findings\n")
        for f in active:
            lines.append(f"### [{f.severity.value.upper()}] {f.title}")
            lines.append(f"\n**Check:** `{f.check_id}`  ")
            lines.append(f"**Category:** {f.category}  ")
            if f.affected_group:
                lines.append(f"**Affected group:** {f.affected_group}  ")
            lines.append(f"**Remediation effort:** {f.remediation_effort.value}  ")
            lines.append(f"\n{f.description}")
            if f.evidence:
                lines.append("\n**Evidence:**")
                for k, v in f.evidence.items():
                    lines.append(f"- `{k}`: {v}")
            lines.append(f"\n**Recommendation:** {f.recommendation}")
            if f.standards_refs:
                lines.append("\n**Standards:**")
                for ref in describe_refs(f.standards_refs):
                    lines.append(f"- {ref}")
            lines.append("")

    if passed:
        lines.append(f"\n## Passed Checks ({len(passed)})\n")
        for f in passed:
            lines.append(f"- ✓ `{f.check_id}` — {f.title}")

    lines.append(f"\n---\n*Generated by rai-audit-core*")
    return "\n".join(lines)


